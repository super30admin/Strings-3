/** Convert a non-negative integer num to its English words representation.
*TC O(N) SC O(N) - recursive stack
*/
class Solution {
    String [] below20;
    String [] tens;
    String [] thousands;
    public String numberToWords(int num) {
        below20 = new String[] {"", "One", "Two", "Three", "Four", "Five", "Six", "Seven", "Eight", "Nine", "Ten", "Eleven", "Twelve", "Thirteen", "Fourteen", "Fifteen", "Sixteen", "Seventeen", "Eighteen", "Nineteen"};
        tens = new String[] {"", "Ten", "Twenty", "Thirty", "Forty", "Fifty", "Sixty", "Seventy", "Eighty", "Ninety"};
        thousands = new String[]{"", "Thousand", "Million", "Billion"};
        if (num == 0) {
            return "Zero";
        }
        String result = "";
        int i = 0;
        while (num > 0) {
            if (num%1000 > 0) {
                result = helper(num%1000) + thousands[i] + " " + result;
            }
            num = num/1000;
            i++;
        }
        return result.trim();
    }
    
    private String helper(int num) {
        if (num == 0) {
            return "";
        }
        else if (num < 20) {
            return below20[num] + " ";
        }
        else if (num < 100) {
            return tens[num/10] + " " + helper(num%10);
        }
        else {
            return below20[num/100] + " Hundred " + helper(num%100);
        }
    }
}
